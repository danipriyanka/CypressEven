 
describe('To learn handling Webtables',()=>{
  it('TC-01 Table1 functionality',()=>{
    let sum=0
    cy.visit("https://webdriveruniversity.com/Data-Table/index.html")
    cy.get('#t01 > tbody').find('tr').each((el,i)=>{
        if(i!=0){
            let text=(el.children().last()).text()
            cy.log(text)
            sum=sum + Number(text)    //0 + 27 //27 + 56 //27+56 +80
        }
    }).then(()=>{
        cy.log(sum)
        expect(sum).to.greaterThan(100)
    })

  })
  it('TC-02 Table1 functionality',()=>{
    let sum2=0
    cy.visit("https://webdriveruniversity.com/Data-Table/index.html")
    cy.get('#t02 > tbody').find('tr').each((el,i)=>{
        if(i!=0){
            let text=Number((el.children().last()).text())
            sum2=sum2 + text
        }
       
    }).then(()=>{
        cy.log(sum2)
        expect(sum2).to.equal(163)
    })
})
})   





































































































































































































































































































































































